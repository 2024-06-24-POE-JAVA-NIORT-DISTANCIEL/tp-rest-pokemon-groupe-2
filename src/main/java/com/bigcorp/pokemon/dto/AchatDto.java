package com.bigcorp.pokemon.dto;

public class AchatDto {
    private Integer id;
    private Integer dresseurId;
    private Integer objetId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getDresseurId() {
        return dresseurId;
    }

    public void setDresseurId(Integer dresseurId) {
        this.dresseurId = dresseurId;
    }

    public Integer getObjetId() {
        return objetId;
    }

    public void setObjetId(Integer objetId) {
        this.objetId = objetId;
    }
}
